        $client = Client::firstOrCreate($request->only(['email', 'cpf']));
        $client->fill($request->all());
        $order = Order::create(['client_id' => $client->id]);
        foreach ($request->input('cart') as $item) {
            $book = Book::where('id', $item['book']['id'])->first();
            OrderItem::create([
                'order_id' => $order->id,
                'book_id' => $book->id,
